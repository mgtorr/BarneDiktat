// Funksjon for å hente tekst etter ID
function getTextById(id) {
    for (const level of Object.values(DICteesDB)) {
        const text = level.find(t => t.id === id);
        if (text) return text;
    }
    return null;
}

// Funksjon for å hente alle tekster på et nivå
function getTextsByLevel(level) {
    return DICteesDB[level] || [];
}

// Funksjon for å hente en tilfeldig tekst på et nivå
function getRandomText(level) {
    const texts = DICteesDB[level] || [];
    return texts[Math.floor(Math.random() * texts.length)];
}

